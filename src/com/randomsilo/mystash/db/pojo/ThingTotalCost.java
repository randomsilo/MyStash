package com.randomsilo.mystash.db.pojo;

public class ThingTotalCost {
	Double totalActualCost;
	Double totalReplacementCost;
	
	public Double getTotalActualCost() {
		return totalActualCost;
	}
	public void setTotalActualCost(Double totalActualCost) {
		this.totalActualCost = totalActualCost;
	}
	public Double getTotalReplacementCost() {
		return totalReplacementCost;
	}
	public void setTotalReplacementCost(Double totalReplacementCost) {
		this.totalReplacementCost = totalReplacementCost;
	}
	
	
}
